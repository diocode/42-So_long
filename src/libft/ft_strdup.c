#include "libft.h"

char	*ft_strdup(const char *src)
{
	int			i;
	char		*str;

	str = (char *) malloc((ft_strlen((char *)src) + 1) * sizeof(char));
	if (!str)
		return (0);
	i = 0;
	while (src[i])
	{
		str[i] = src[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
